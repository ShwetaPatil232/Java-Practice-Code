import java.lang.*;

public class OneTo20Tables {

    public static void main(String args[]){

        for(int i=1; i<=20; i++){

            for(int j=1; j<=10; j++){
                System.out.print("\t "+(i*j));
            }
            System.out.println("\n");
        }

        System.out.println("\n\n");
    }
    
}
